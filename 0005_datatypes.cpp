
// Data Types - 3 Types
// ---> Built-in datatype - int,float,char,double,bool
// ---> User-defined - struct,union,enum
// ---> Derived - Array, Function,Pointer 

#include<iostream>
using namespace std;

int main()
{
    int a=1,b=2;
    cout<<"The value of a is "<<a<<" and the value of b is "<<b<<endl;
    float c = 12.234;
    cout<<"The value of float c is "<<c<<endl;
    char d = 's';
    cout<<"The value of char d is "<<d<<endl;
    double e = 12.234567; // As higher precision than float
    cout<<"The value of double e is "<<e<<endl;
    bool f = true;
    cout<<"The value of bool f is "<<f<<endl;
    return 0;
}