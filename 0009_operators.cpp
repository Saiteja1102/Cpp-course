// Operators in c++
#include<iostream>
using namespace std;

int main()
{
    int a=4,b=7;
    cout<<"Types of Operators in c++: "<<endl;
    cout<<endl;
    cout<<"1. Arithmetic operators: "<<endl;
    cout<<"The value of a + b is "<<a+b<<endl;
    cout<<"The value of a - b is "<<a-b<<endl;
    cout<<"The value of a * b is "<<a*b<<endl;
    cout<<"The value of a / b is "<<a/b<<endl;
    cout<<"The value of a % b is "<<a%b<<endl;
    cout<<"The value of ++a is "<<++a<<endl;
    cout<<"The value of --a is "<<--a<<endl;
    cout<<"The value of a++ is "<<a++<<endl;
    cout<<"The value of a-- is "<<a--<<endl;
    cout<<endl;
    cout<<endl;

    cout<<"2. Assignment Operators: "<<endl;
    cout<<"Used to asign values to variables\n examples: int a=3,b=9;"<<endl;
    cout<<endl;
    cout<<endl;

    cout<<"3. Comparision operators: "<<endl;
    cout<<"The values of a==b is "<<(a==b)<<endl;
    cout<<"The values of a!=b is "<<(a!=b)<<endl;
    cout<<"The values of a>=b is "<<(a>=b)<<endl;
    cout<<"The values of a<=b is "<<(a<=b)<<endl;
    cout<<"The values of a>b is "<<(a>b)<<endl;
    cout<<"The values of a<b is "<<(a<b)<<endl;
    cout<<endl;
    cout<<endl;

    cout<<"4. Logical operators: "<<endl;
    cout<<"The value of this logical and operator ((a==b) && (a<b)) is "<<((a==b) && (a<b))<<endl;
    cout<<"The value of this logical or operator ((a==b) || (a<b)) is "<<((a==b) || (a<b))<<endl;
    cout<<endl;
    cout<<endl;
}