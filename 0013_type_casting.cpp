#include<iostream>
using namespace std;

int main()
{
    cout<<endl;
    cout<<"***** Type casting *****"<<endl;
    cout<<endl;

    int num1 = 54;
    float num2 = 54.54;
    cout<<"the value of num1 is "<<num1<<endl;
    cout<<"the value of num2 is "<<num2<<endl;
    cout<<"the value of float num1 is "<<float(num1)<<endl;
    cout<<"the value of int num2 is "<<int(num2)<<endl;
    cout<<"the value of int num2 is "<<(int)num2<<endl;
    cout<<"the value of float num1 is "<<(float)num1<<endl;
    cout<<endl;
}