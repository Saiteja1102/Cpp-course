/*

Rules:
1. Variable names ranges from 1 to 255 characters
2. Must begin with alphabet or underscore
3. Names are case sensitive
4. No spaces and special characters are allowed
5. Don't use keywords as names

*/

#include<iostream>
using namespace std;

int main()
{
    int a = 1; // int 1,2,3,-1,0,-5
    float b = 1.2; // float 1.2,23.009,-1.43
    char c = 's'; // char 'q','f','a'
    cout<<a<<" "<<b<<" "<<c<<endl;
    return 0;
}